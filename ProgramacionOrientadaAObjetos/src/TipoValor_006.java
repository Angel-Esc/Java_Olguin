public class TipoValor_006 { // Inicio de la clase
    public static void main(String[] args) { //Inicio del Main

        //Uso de la palabra var
        System.out.println("Uso de la palabra var para declarar variables");
        var nombre = "Angel Escamilla";
        System.out.println("Mi nombre es: " + nombre);
        var edad = 36;
        System.out.println("Mi edad es: " + edad);
        var estatura = 1.68f;
        System.out.println("Mi estatura es: " + estatura);


        // Uso de variables comunes
        System.out.println(); //Espacio en blanco
        String nombre2 = "Angel Escamilla";
        System.out.println("Nombre es:" + nombre2);
        int edad2 = 25;
        System.out.println("Edad es de: " + edad2);
        float estatura2 = 1.56f;
        System.out.println("Estatura es de: " + estatura2);

    } //Fin del main
} // Fin de la clase
