public class OperadoresComparacion_027 {
    public static void main(String[] args) {

        System.out.println("*** Operadores de Comparacion ***");

        int a = 5, b = 3;
        
        //Igual a
        System.out.println("Resultado de a == b: " + (a == b));
        //diferente a
        System.out.println("Resultado de a != b: " + (a != b));
        //Mayor a
        System.out.println("Resultado de a > b>: " + (a > b));
        //Menor a
        System.out.println("Resultado de a < b: " + (a < b));
        //Igual o mayor a
        System.out.println("Resultado de a >= b: " + (a >= b));
        //Igual o menor a
        System.out.println("Resultado de a <= b: " + (a <= b));




    }
}
